interface I18nContextType {
    currentLang: string;
    setCurrentLang: (lang: string) => void;
    t: (key: string) => string;
    terminalT: (key: string) => string;
    getRepoKeys: () => string[];
}

const I18nContext = createContext<I18nContextType | undefined>(undefined);

export const I18nProvider = ({ children }: { children: ReactNode }) => {
    const [currentLang, setCurrentLang] = useState('en');

    useEffect(() => {
        const userLang = navigator.language.toLowerCase();
        if (userLang.startsWith('pt')) setCurrentLang('pt');
        else if (userLang.startsWith('es')) setCurrentLang('es');
        else setCurrentLang('en');
    }, []);

    const t = (key: string): string => {
        const keys = key.split('.');
        const getValue = (lang: string) => {
            let value = translations[lang];
            for (const k of keys) {
                value = value?.[k];
            }
            return value;
        };

        const result = getValue(currentLang) || getValue('en');
        return (typeof result === 'string' ? result : key);
    };

    const terminalT = (key: string): string => {
        return t(`terminal.${key}`);
    };

    const getRepoKeys = (): string[] => {
        return Object.keys(translations.en.repo_descriptions || {});
    };

    const contextValue = {
        currentLang,
        setCurrentLang,
        t,
        terminalT,
        getRepoKeys
    };

    return (
        <I18nContext.Provider value={contextValue} >
            {children}
        </I18nContext.Provider>
    );
};

export const useI18n = () => {
    const context = useContext(I18nContext);
    if (!context) {
        throw new Error('useI18n must be used within an I18nProvider');
    }
    return context;
};
